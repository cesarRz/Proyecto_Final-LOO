    public Detalle(int event_id) {
        initComponents();
        setDefaultCloseOperation(javax.swing.WindowConstants.DISPOSE_ON_CLOSE);
        Base base = new Base();

        try {
            ResultSet evento = base.getEvento(event_id);
            jTitletxt.setText(evento.getString("titulo"));
            jSalatxt.setText(evento.getString("id_sala"));
            jFechatxt.setText(evento.getString("fecha"));
            jHoratxt.setText(evento.getString("hora"));
            base.closeBase(); 

            fillEventos(event_id);
            
            
        } catch (HeadlessException | SQLException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }       
    }

