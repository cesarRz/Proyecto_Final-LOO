    private void fillEventos(int event_id) throws SQLException{
        tabla = (DefaultTableModel) jTable1.getModel();
        Base base = new Base();
        ResultSet resultado = base.getReservacionbyEvent(event_id);
        Object object[] = new Object[5];

        if (resultado.next() ==false) {
            JOptionPane.showMessageDialog(null,"No existen reservaciones", "Error", JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
            
        }else{
            this.setVisible(true);
            do{
                object[0] = resultado.getString("nombre");
                object[1] = resultado.getString("apellido");
                object[2] = resultado.getString("no_asiento");
                object[3] = resultado.getString("fecha");
                object[4] = resultado.getString("hora");
                tabla.addRow(object);
            }while(resultado.next());

        }  
        base.closeBase();
    }


